%% 程序初始化，清空工作空间，缓存，
clear all;
close all;
clc;
% 读取Octave控制数据库（注：如使用Matlab，可删除或注释掉本行代码）
pkg load control;
%% 定义传递函数方法一
%% 定义符号"s"
s = tf('s');
%% 构建系统传递函数G_s
G_s = (s+1)/(s+3)
%% 定义传递函数方法二
%% 使用tf语句定义控制器传递函数C_s
C_s =  tf([1],[1 2])
%% 使用feedback语句定义反馈闭环传递函数
Gcl = feedback(C_s*G_s,1)

